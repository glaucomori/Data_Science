saldo = 500
print(saldo)

saldo = 200
print(saldo)

saldo += 10     # adiciona o valor e atribui
print(saldo)

saldo -= 5      # subtrai o valor e atribui
print(saldo)

saldo //= 2     # número inteiro após a divisão do valor (ignorando as casas decimais) e atribui
print(saldo)

saldo /= 2      # divide pelo valor e atribui
print(saldo)

saldo *= 10     # multiplica pelo valor e atribui
print(saldo)

saldo %= 4      # obtém o resto da divisão inteira pelo valor e atribui
print(saldo)

saldo **= 2     # calcula a exponenciação pelo valor e atribui
print(saldo)